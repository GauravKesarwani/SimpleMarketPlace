package viewPackage;

public class SoldItems {
	private String itemName;
	private String availquan;
	private String soldQuan;
	private String userid;
	private String sellerInfo;
	
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getAvailquan() {
		return availquan;
	}
	public void setAvailquan(String availquan) {
		this.availquan = availquan;
	}
	public String getSoldQuan() {
		return soldQuan;
	}
	public void setSoldQuan(String soldQuan) {
		this.soldQuan = soldQuan;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getSellerInfo() {
		return sellerInfo;
	}
	public void setSellerInfo(String sellerInfo) {
		this.sellerInfo = sellerInfo;
	}	

}
